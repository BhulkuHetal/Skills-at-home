public class AssignmentOperators {

    public static void main(String[] args) {

        int a =10;
        int b =20;
        int c;
        System.out.println(c=a+b);
        System.out.println(c+=a);
        System.out.println(c-=a);
        System.out.println(c*=a);
        System.out.println(c/=a);
        System.out.println(c%=a);
        System.out.println(c^=a);

        int i = 50;
        System.out.println(i<10&&i<20);
        System.out.println(i<10);

    }
}
