public class RelationalOperators {
    public static void main(String[] args){

        int A = 10;
        int B = 40;



        System.out.println(A==B);
        System.out.println(A!=B);
        System.out.println(A > B);
        System.out.println(A < B);
        System.out.println(A>=B);
        System.out.println(A<=B);

        System.out.println(A<10&&A<20);
        System.out.println(!(A<10&&A<20));



    }
    }

